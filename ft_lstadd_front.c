#include <stdio.h>
#include <unistd.h>
#include <string.h>
#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!new)
		return ;
	if (!lst)
		return ;
	new->next = *lst;
	*lst = new;
}
