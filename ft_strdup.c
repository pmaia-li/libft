#include <stdio.h>
#include <unistd.h>
#include <string.h>
#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;
	int		len;

	len = ft_strlen(src);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
